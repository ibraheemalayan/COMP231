package Revision.types;

public class DataTypes {

    public static void main(String[] args) {


    /// type ///////////////////// size in bits ////////////////////////////////////

        byte myByte = 122;             // 8  // Byte (integer between -128 and 127)
        short myShort = 5;             // 16 //  Short Integer
        int myNum = 5;                 // 32 //  Integer (whole number)
        long myLong = 3456789875565L;  // 64 //  Long Integer
  
        float myFloatNum = 5.99F;      // 32 //  Floating point number
        double myDoubleNum = 19.4567;  // 64 //  Double Floating point number
  
        char myLetter = 'D';           // 16 //  Character
          
        boolean myBool = true;         // 1  //  Boolean
  
        String myText = "Hello";       // 1  //  String


        


    }
}
